document.addEventListener('DOMContentLoaded', function() {
    const form = document.getElementById('gform_1');
    const submitBtn = form.querySelector('button[type="submit"]');

    // Track answered questions
    let answeredCount = 0;
    const totalQuestions = {{ count($gejala) }};

    function updateSubmitButton() {
        if (answeredCount > 0) {
            submitBtn.disabled = false;
            submitBtn.innerHTML = '<i class="bi bi-clipboard2-check me-2"></i>Proses Diagnosis (' + answeredCount + '/' + totalQuestions + ')';
        } else {
            submitBtn.disabled = true;
            submitBtn.innerHTML = '<i class="bi bi-clipboard2-check me-2"></i>Jawab Minimal 1 Pertanyaan';
        }
    }

    // Initial state
    updateSubmitButton();

    // Listen for radio button changes
    document.querySelectorAll('input[name^="answers"]').forEach(function(radio) {
        radio.addEventListener('change', function() {
            const questionId = this.name.match(/\[(\d+)\]/)[1];
            const questionRadios = document.querySelectorAll('input[name="answers[' + questionId + ']"]');

            // Check if this question was previously answered
            let wasAnswered = false;
            questionRadios.forEach(function(r) {
                if (r.checked) wasAnswered = true;
            });

            if (this.checked && !wasAnswered) {
                answeredCount++;
            } else if (!this.checked && wasAnswered) {
                answeredCount--;
            }

            updateSubmitButton();
        });
    });

    // Form submission
    form.addEventListener('submit', function(e) {
        if (answeredCount === 0) {
            e.preventDefault();
            alert('Silakan jawab minimal satu pertanyaan.');
            return false;
        }

        // Show loading state
        submitBtn.disabled = true;
        submitBtn.innerHTML = '<span class="spinner-border spinner-border-sm me-2"></span>Memproses...';
    });
});
